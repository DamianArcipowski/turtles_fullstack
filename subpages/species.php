    <p><span>Wygląd i cechy charakterystyczne</span> <br>
        Żółw błotny ma owalny, ciemny karapaks pokryty drobnymi, żółtawymi kropkami i liniami. Dorosłe osobniki osiągają zazwyczaj 12–20 cm długości. Samice są zwykle większe od samców, a ich ogon jest krótszy i szerszy u nasady. Młode żółwie są znacznie jaśniejsze, często oliwkowe, z wyraźnymi wzorami na pancerzu.
    </p>
    <p><span>Tryb życia i zachowanie</span> <br>
        Swoją roczną aktywność żółwie rozpoczynają po zakończeniu snu zimowego, który trwa od jesieni do wczesnej wiosny. Po wyjściu z hibernacji przystępują do okresu godowego. Samce stają się wtedy bardzo ruchliwe i aktywnie poszukują samic, wykonując charakterystyczne zachowania zalotne.
        Latem żółwie najwięcej czasu spędzają w wodzie, gdzie polują na owady, małe ryby, larwy płazów i inne bezkręgowce. Chętnie też wygrzewają się na brzegach, konarach lub pływających kępach roślinności, by podnieść temperaturę ciała – jako zmiennocieplne, są mocno zależne od warunków pogodowych. Jesienią ich aktywność stopniowo spada, aż ponownie zapadają w zimową hibernację, zwykle w mule na dnie zbiornika.
    </p>
    <p><span>Rozmnażanie i rozwój</span> <br>
        Samice składają jaja w okresie od maja do czerwca, wybierając dobrze nasłonecznione, piaszczyste miejsca z dala od wody. W jednym gnieździe może znaleźć się od 5 do 15 jaj. Inkubacja trwa zazwyczaj 60–70 dni, a młode wykluwają się latem. Ich przeżywalność w naturze jest jednak niska – zagrożenia stanowią zarówno drapieżniki, jak i trudne warunki środowiskowe.
    </p>
